class BandMember:
    def __init__(self, member_id, username, password, name, instruments, experience_level, location, collaboration_type, availability, social_links, teams):
        self.member_id = member_id
        self.username = username
        self.password = password
        self.name = name
        self.instruments = self.instruments
        self.experience_level = experience_level
        self.location = location
        self.collaboration_type = collaboration_type
        self.availability = availability
        self.social_links = social_links
        self.teams = teams